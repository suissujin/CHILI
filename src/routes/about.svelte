    <p>
      Diese Blogpage ist das Produkt der Hausaufgabe für die Anmeldung an die
      Zürcher Hochschule der Künste. Das Konzept dieser Hausaufgabe ist
      «unscharf».
    </p>
    <p>
      Die Idee entstand aus dem dazugehörigem Synonym «unklar». Unscharf nicht
      im Sinne von visuell unerkennbar, sondern unverständlich. Es sollte ein
      Produkt werden, welches trotz vollkommener visueller Klarheit mit einer
      anderen Form von Unschärfe spielt. Der Satz «instructions unclear» spielte
      eine zentrale Rolle beim Designen des Projekts und führte zur Idee der
      «How to» - Seite.
    </p>
    <p>
      Um den gewünsten Effekt der absoluten visuellen Klarheit zu haben,
      gestaltete ich die Seite so intuitiv, modern und responsive wie möglich.
      Sie sollte einfach und befriedigend zu benutzen sein. Der «unscharfe» Teil
      sollte der Inhalt sein und dem Besucher verwirren und aufhalten. Dem
      Besucher anschliessend die Möglichkeit zu geben diese Verwirrung zu lösen,
      verbindet diesen Teil mit der intuitiven Benutzung. Dieser Approach eine
      Webseite zu machen sehe ich als Art «Gamification».
    </p>
    <p>
      Das Besuchen und Interagieren mit der Seite an sich, ist nicht wirklich
      ein Spiel. Mit dem intuitiven Designen des Benutzerinterfaces, ist das
      Navigieren auf der Webseite aber als Spiel gestaltet. Das Design der Seite
      ist modern gehalten und passend zum Portfolio-Konzept brutal und
      monochrome.
